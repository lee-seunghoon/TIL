# 대입 연산자
# = : 오른쪽 데이터를 왼쪽 변수에 저장
# 대입 연산자는 산술기호만 가능

s1 = 100
print(s1)

s1 = s1 + 100
print(s1)

s1 +=200  # s1 = s1 + 200
print(s1)

s1 -= 300 # s1 = s1 - 300
print(s1)

s1 *= 100 # s1 = s1 * 100
print(s1)

s1/=20  # s1 = s1 / 20
print(s1)

s1 **= 2   # s1 = s1 ** 2
print(s1)

s1 //= 5  # s1 = s1 // 5
print(s1)

s1 %= 2   # s1 = s1 % 2
print(s1)
