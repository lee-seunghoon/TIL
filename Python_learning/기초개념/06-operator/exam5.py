# 관계 연산자 (단독으로 쓸 일은 거의 없고, 제어문과 연동해서 쓴다.)
# 왼쪽 값과 오른 똑 값을 비교해서 식이 옳은지 검사하는 연산자
# --> 식이 옳으면 : True / 옳지 않으면 : False
# 1) 비교 연산자 : <, >, <=, >=
# 2) 동등 연산자 : ==, !=

num1 = int(input("정수 입력 : "))
num2 = int(input("정수 입력 : "))
print(num1 > num2)
print(num1 < num2)
print(num1 >= num2)
print(num1 <= num2)
print(num1 == num2)  # 같은지 검사
print(num1 != num2)  # 같지 않은지 검사
 

